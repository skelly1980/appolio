                    <h2>What Is Cultural Competency?</h2>
                    <p>If you work in healthcare HR, you’ve probably heard the phrase cultural competency before.
                        So what exactly does it mean? Put simply, <span>cultural competency describes</span> how well an organization
                        or individual is able to interact with a wide range of beliefs. For an employer, this means putting
                        active effort into defining and understanding the culture of those it serves and employs. Cultural
                        competency training can help you decrease bias within your organization and stay compliant. Here’s
                        what you need to know. </p>
                    <div class="img-container">
                        <img src="assets/faces.png" alt="Three faces">
                    </div>
                    <h2>Why Is Cultural Competency Important?</h2>
                    <p>When an organization doesn’t practice cultural competency, it can lead to a wide range of ill effects.
                        Not only will certain groups feel disempowered to seek care, but the <span>quality of care can also decrease.</span></p>
                    <div class="proven-container">
                        <h3>It's Proven: Bias Restricts Care in Healthcare</h3>
                        <ul>
                            <li><b>Ageism:</b> is discrimination on the basis of someone’s age. A common type of bias that can especially affect the populations in skilled nursing facilities and senior
                                living communities, ageism can cause symptoms to be dismissed. One
                                study found that ageism leads to worse <span>outcomes 95.5% of the
                                    time.</span></li>
                            <li><b>Gender Inequality:</b> Discrimination on the basis of sex and gender can affect health. <span>In 2019, an analysis in Denmark</span> found
                                that women waited longer than men for a diagnosis in 73% of cases. </li>
                            <li><b>Racism:</b> Racial bias permeates every part of healthcare. This has been particularly evident during the COVID-19 pandemic,
                                where one study found that Black people in the US were <span>3.57 times more likely to die</span> from COVID-19 than white people.</li>
                        </ul>
                        <p class="image-container"><img src="assets/health-disparities.webp" alt="Health Disparities" width="803" height="452"></p>
                        <p>Photo courtesy of KFF</p>
                        <p>In addition to what we’ve covered here, classism, sexuality, fatphobia, nationality, and more can affect a resident’s ability to receive the care they deserve.
                            Cultural competency training can help create a welcoming environment for both patients and employees. Everyone deserves to feel comfortable at work.</p>
                        <h2>How Does Cultural Competency Factor Into Compliance? </h2>
                        <p>Cultural competency training is regulated by <span>CMS guidelines</span> in several areas. </p>
                        <p>One major area is <span>Ftag 553</span>, which mandates that organizations “incorporate the resident’s personal and cultural preferences in developing goals of care.” </p>
                        <p>Ftag 675 also points out that when residents aren’t treated in a culturally competent manner, quality of care decreases. </p>
                        <p>Regardless of whether or not CMS monitors a specific type of bias, it’s important to continually update and educate your staff
                            on how to make your workplace a more safe and equitable place.</p>
                        <h2>How to Start Cultural Competency Training in Healthcare </h2>
                        <p><span>According to the CDC</span>, these are the factors that should be addressed during cultural competency training. </p>
                        <ul class="list-2">
                            <li>Define culture broadly.</li>
                            <li>Value clients’ cultural beliefs.</li>
                            <li>Recognize complexity in language interpretation.</li>
                            <li>Facilitate learning between providers and communities.</li>
                            <li>Involve the community in defining and addressing service needs.</li>
                            <li>Collaborate with other agencies.</li>
                            <li>Professionalize staff hiring and training.</li>
                            <li>Institutionalize cultural competence.</li>
                        </ul>
                        <p>We know that some of these steps are clearer than others. Let’s break it down. </p>
                        <h3>Define Culture Broadly</h3>
                        <p>Understand the myriad of identities that your residents have and the way identityaffects how they move through the world. </p>
                        <p>Culture can include things such as race, religion, <span>gender</span>, sexuality, <span>disability</span>, nationality, language, ethnicity, and socioeconomic status. Culture can also extend to lived experiences that can’t be simply defined. </p>
                        <p>When you train your staff to understand what culture is, you’re more likely to ensure that your residents won’t feel unseen or unheard. </p>
                        <h2><a href="#" target="_blank"><img src="assets/ready-image.webp" alt="Ready to See How Healthcare Hires?"></a></h2>
                        <h3>Value Clients’ Cultural Beliefs </h3>
                        <p>Welcome your patients’ cultural beliefs by offering space to share and listen. Create channels for both residents and staff to
                            raise questions or concerns. For staff, that might be pulse surveys or check-in meetings. Talk to patients regularly and keep
                            notes on any complaints (or compliments) regarding their care. </p>
                        <p>As you navigate cultural competency, make sure your whole team understands the stakes. Educate your staff about the communities they serve, including the ways certain groups have been overlooked in healthcare.
                            Bring in outside speakers and resources if necessary. </p>
                        <h3>Recognize Complexity in Language Interpretation</h3>
                        <p>Whenever possible, use interpreters for residents who need to communicate in a language other than English. Make sure these interpreters are appropriately trained to work with patients, including members of the deaf community. </p>
                        <p>Even with the best interpreters, interpretation can be flawed—especially when describing subjective symptoms. Create a protocol for how to support patients who need interpreters (or have family members who’d like to interpret for them),
                            and make this information publicly accessible on your website.</p>
                        <h3>Facilitate Learning Between Providers and Communities </h3>
                        <p>When your facility is disconnected from the people you serve, it’s much harder to actually carry out your mission. Consider events, speakers, fundraisers, and more to connect to the surrounding population. These events can be focused
                            around the latest topics in healthcare and provide helpful public information. </p>
                        <p>A robust volunteer program can also invite involvement from the local community. Unable to invest resources in in-person events? Social media can be a great way to build your brand and establish a voice. </p>
                        <p><a href="#" target="_blank"><span>Learn more about social media</span></a></p>
                        <h3>Involve the Community in Defining and Addressing Service Needs</h3>
                        <p>Collect feedback from your community of residents. How effectively do they feel your staff is trained? Take note of both positive and negative comments. </p>
                        <p>It’s important to grow and adjust to criticism, but it’s also vital to recognize your staff for their efforts. You can also survey your employees in order to better understand how they feel about your cultural competency training. </p>
                        <p><a href="#" target="_blank"><span>Learn more about employee surveys</span></a></p>
                        <p class="image-container"><img src="assets/icons_employer_green.svg" alt="Worker sitting at desk"></p>
                        <h3>Collaborate With Other Agencies </h3>
                        <p>It’s unlikely that any one member of staff is an expert in absolutely all cultural issues. It’s important to learn as much as possible, but also to acknowledge what you don’t know. Call on partners, educators, and other agencies to fill in the gaps. </p>
                        <h3>Professionalize Staff Hiring and Training</h3>
                        <p>Your candidates (and employees) come to this work with different levels of knowledge and lived experience. Use standardized training to make sure everyone’s on the same page during hiring. </p>
                        <p>You can standardize your hiring by developing a set of interview questions and communications that stays consistent for every candidate and employee, so <span>implicit biases</span> don’t color the interview process. This doesn’t mean that your communication has to be robotic, of course. Instead, standardization ensures that every candidate has the same opportunities to show what makes them special. </p>
                        <p><a href="#" target="_blank"><span>Learn how to standardize messaging here</span></a></p>
                        <h3>Institutionalize Cultural Competence </h3>
                        <p>Ultimately, cultural competence needs to be an undeniable part of your staff’s daily routine. </p>
                        <p>This can feel a bit abstract, but there are concrete places to begin. </p>
                        <p>Make sure every staff member has standard knowledge of how bias can affect patients in healthcare, and do your best to provide avenues for further education. Create situations where residents and employees can raise questions and concerns freely, such as HR office hours or town halls. The more consistently you focus on achieving cultural competency, the better results you’ll receive. </p>
                        <h3>Resources for Cultural Competency Training </h3>
                        <p>Culture competency is a complex issue that should be an ongoing conversation among your staff. What we’ve covered here is only part of the guidelines set by CMS. </p>
                        <p><a href="#" target="_blank"><span>Learn more about CMS’s comprehensive guidelines</span></a></p>
